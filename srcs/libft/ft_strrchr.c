#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int i;

	i = ft_strlen(s);
	if (s[i] == '\0' && c == '\0')
		return ((char*)s + (i));
	while (s[i] != c && i + 1 > 0)
		i--;
	if (i + 1 == 0)
		return (0);
	return ((char*)s + i);
}
