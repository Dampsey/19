#include "libft.h"

int				ft_lstsize(t_list *lst)
{
	unsigned int i;

	i = 0;
	if (lst)
	{
		i = 1;
		while (lst->next != NULL)
		{
			lst = lst->next;
			i++;
		}
	}
	return (i);
}
