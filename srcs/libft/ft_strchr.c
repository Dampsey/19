#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int i;

	i = 0;
	while (s[i] != '\0' && s[i] != c)
	{
		if (c == '\0' && s[i + 1] == '\0')
			return ((char*)s + (i + 1));
		i++;
	}
	if (s[i] == c)
		return ((char*)s + i);
	return (NULL);
}
